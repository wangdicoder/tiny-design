function collectLocalCssVars(files) {
  const definedVars = new Set();

  for (const filePath of files) {
    const sourceText = fs.readFileSync(filePath, 'utf8');
    const definitionRegex = /--ty-[a-z0-9-]+(?=['"]?\s*:)/g;
    let match;

    while ((match = definitionRegex.exec(sourceText)) !== null) {
      definedVars.add(match[0]);
    }
  }

  return definedVars;
}

function extractTyVarReferences(sourceText) {
  const references = [];
  const referenceRegex = /var\(\s*(--ty-[a-z0-9-]+)\s*(,|\))/g;
  let match;

  while ((match = referenceRegex.exec(sourceText)) !== null) {
    references.push({
      name: match[1],
      hasFallback: match[2] === ',',
      index: match.index,
    });
  }

  return references;
}

function collectOverridePrefixes(tokens) {
  const prefixes = new Set(
    tokens.filter((token) => token.category === 'component' && token.component).map((token) => token.component)
  );

  for (const directoryName of listDirectories(REACT_SRC_DIR)) {
    prefixes.add(directoryName);
  }

  return prefixes;
}

function isComponentOverrideHook(cssVar, hasFallback, overridePrefixes) {
  if (!hasFallback) return false;

  const body = cssVar.replace(/^--ty-/, '');
  const parts = body.split('-');

  for (let i = parts.length - 1; i >= 1; i -= 1) {
    const prefix = parts.slice(0, i).join('-');
    if (overridePrefixes.has(prefix)) {
      return true;
    }
  }

  return false;
}

function validateConsumerContracts(tokens) {
  const registryVars = new Set(tokens.map((token) => tokenKeyToCssVar(token.key)));
  const consumerFiles = [...listSourceFiles(REACT_SRC_DIR), ...listSourceFiles(DOCS_SRC_DIR)];
  const definedVars = collectLocalCssVars(consumerFiles);
  const overridePrefixes = collectOverridePrefixes(tokens);
  const usedRegistryVars = new Set();
  const errors = [];

  for (const filePath of consumerFiles) {
    const sourceText = fs.readFileSync(filePath, 'utf8');
    const relativePath = path.relative(ROOT, filePath);

    for (const reference of extractTyVarReferences(sourceText)) {
      if (registryVars.has(reference.name)) {
        usedRegistryVars.add(reference.name);
        continue;
      }

      if (definedVars.has(reference.name)) {
        continue;
      }

      if (isComponentOverrideHook(reference.name, reference.hasFallback, overridePrefixes)) {
        continue;
      }

      errors.push(
        `${relativePath}:${lineNumberForIndex(sourceText, reference.index)} references unknown token var "${reference.name}".`
      );
    }
  }

  if (errors.length > 0) {
    throw new Error(formatValidationError('Consumer token contract validation failed.', errors));
  }

  return {
    filesChecked: consumerFiles.length,
    usedRegistryVars: usedRegistryVars.size,
    totalRegistryVars: registryVars.size,
  };
}

function buildRuntimeTokens(options = {}) {
  const shouldWriteArtifacts = options.write !== false;
  const shouldValidateConsumers = options.verifyConsumers !== false;
  const themeSchemaConfig = loadThemeSchemaConfig();
  const actionLabel = shouldWriteArtifacts ? 'Building runtime tokens...' : 'Validating runtime tokens...';

  console.log(`${actionLabel}\n`);
  syncCompileBrandThemeRuntime({ write: shouldWriteArtifacts });

  const primitiveTokens = loadTokenFiles(PRIMITIVE_DIR, 'primitive');
  const semanticTokens = loadTokenFiles(SEMANTIC_DIR, 'semantic');
  const componentTokens = loadTokenFiles(COMPONENT_DIR, 'component');
  const allTokens = [...primitiveTokens, ...semanticTokens, ...componentTokens];
  const tokenValidation = validateTokens(allTokens, themeSchemaConfig);

  if (tokenValidation.errors.length > 0) {
    throw new Error(formatValidationError('Token validation failed.', tokenValidation.errors));
  }

  const themes = loadThemes();
  const themeValidation = validateThemeRegistryUsage(themes, allTokens, themeSchemaConfig);
  if (themeValidation.errors.length > 0) {
    throw new Error(formatValidationError('Theme validation failed.', themeValidation.errors));
  }

  if (themeValidation.warnings.length > 0) {
    console.warn(formatValidationError('Theme validation warnings.', themeValidation.warnings));
  }

  const tokenMap = tokenValidation.tokenMap;
  const cssValues = new Map(allTokens.map((token) => [token.key, toCssValue(token.$value, tokenMap)]));
  const registry = buildRegistry(allTokens, tokenMap);
  const lightTheme = themes.find((theme) => theme.mode === 'light');
  const darkTheme = themes.find((theme) => theme.mode === 'dark');
  const presets = buildPresetMap(themes);

  if (shouldValidateConsumers) {
    const consumerSummary = validateConsumerContracts(allTokens);
    console.log(
      `  consumer contract check (${consumerSummary.filesChecked} files, ${consumerSummary.usedRegistryVars}/${consumerSummary.totalRegistryVars} registry vars referenced)`
    );
  }

  if (!shouldWriteArtifacts) {
    console.log('\nRuntime tokens validated.');
    return { registry, presets };
  }

  const lightThemeOverrides = {
    ...((lightTheme && lightTheme.tokens && lightTheme.tokens.semantic) || {}),
    ...((lightTheme && lightTheme.tokens && lightTheme.tokens.components) || {}),
  };
  const darkThemeOverrides = {
    ...((darkTheme && darkTheme.tokens && darkTheme.tokens.semantic) || {}),
    ...((darkTheme && darkTheme.tokens && darkTheme.tokens.components) || {}),
  };
  const lightCss = buildThemeCss(allTokens, cssValues, tokenMap, lightThemeOverrides, ':root');
  const darkCss = buildThemeCss(allTokens, cssValues, tokenMap, darkThemeOverrides, "[data-tiny-theme='dark']");
  const baseCss = buildBaseThemeCss(allTokens, cssValues, tokenMap, lightTheme, darkTheme);

  const primitiveTier = allTokens.filter((token) => token.category === 'primitive');
  const semanticTier = allTokens.filter((token) => token.category === 'semantic');
  const foundationCss = buildSliceCss(primitiveTier, cssValues, tokenMap, lightTheme, darkTheme);
  const semanticCss = buildSliceCss(semanticTier, cssValues, tokenMap, lightTheme, darkTheme);

  const { tokensByNamespace, manifest } = computeSliceManifest(allTokens);

  mkdirp(DIST_CSS_DIR);
  mkdirp(SCHEMA_DIST_DIR);
  const componentsDir = path.join(DIST_CSS_DIR, 'components');
  mkdirp(componentsDir);
  writeJson(path.join(DIST_DIR, 'registry.json'), registry);
  writeJson(path.join(DIST_DIR, 'presets.json'), presets);
  fs.writeFileSync(path.join(DIST_CSS_DIR, 'light.css'), lightCss);
  fs.writeFileSync(path.join(DIST_CSS_DIR, 'dark.css'), darkCss);
  fs.writeFileSync(path.join(DIST_CSS_DIR, 'base.css'), baseCss);
  fs.writeFileSync(path.join(DIST_CSS_DIR, 'foundation.css'), foundationCss);
  fs.writeFileSync(path.join(DIST_CSS_DIR, 'semantic.css'), semanticCss);

  for (const [namespace, namespaceTokens] of tokensByNamespace.entries()) {
    const sliceCss = buildSliceCss(namespaceTokens, cssValues, tokenMap, lightTheme, darkTheme);
    fs.writeFileSync(path.join(componentsDir, `${namespace}.css`), sliceCss);
  }

  writeJson(path.join(DIST_CSS_DIR, 'component-deps.json'), manifest);

  fs.writeFileSync(REGISTRY_DTS_PATH, buildRegistryDts());
  fs.writeFileSync(PRESETS_DTS_PATH, buildPresetsDts(presets));
  fs.copyFileSync(THEME_SCHEMA_PATH, path.join(SCHEMA_DIST_DIR, 'theme.schema.json'));

  console.log('  dist/registry.json');
  console.log('  dist/registry.d.ts');
  console.log('  dist/presets.json');
  console.log('  dist/presets.d.ts');
  console.log('  dist/schema/theme.schema.json');
  console.log('  dist/css/light.css');
  console.log('  dist/css/dark.css');
  console.log('  dist/css/base.css');
  console.log('  dist/css/foundation.css');
  console.log('  dist/css/semantic.css');
  console.log(`  dist/css/components/*.css (${tokensByNamespace.size} slices)`);
  console.log(`  dist/css/component-deps.json (${Object.keys(manifest).length} dirs)`);
  console.log('\nRuntime tokens done.');

  return { registry, presets };
}

function parseCliArgs(argv) {
  return {
    write: !argv.includes('--validate-only'),
    verifyConsumers: !argv.includes('--no-consumer-check'),
  };
}

module.exports = { buildRuntimeTokens };

if (require.main === module) {
  const cliOptions = parseCliArgs(process.argv.slice(2));

  try {
    buildRuntimeTokens(cliOptions);
  } catch (err) {
    console.error(err);
    process.exit(1);
  }
}
